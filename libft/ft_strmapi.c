#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	length;
	char	*str;

	if (s)
	{
		i = 0;
		length = ft_strlen(s);
		str = (char*)malloc(length + 1 * sizeof(char));
		if (str == NULL)
			return (NULL);
		while (s[i] != '\0')
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
